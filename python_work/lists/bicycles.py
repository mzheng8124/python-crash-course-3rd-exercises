# notes: list indexes start at 0 not 1..


# list example
bicycles = ['trek', 'cannondale', 'redline', 'specialized']
print(bicycles)

# taking a single element from the list
print(bicycles[0])

# using title() method
print(bicycles[0].title())

# convention for returning the last item in a list
print(bicycles[-1])

# using value in a varaible then output 
message = f"My first bicycle was a {bicycles[0].title()}."
print(message)




